<template>
     <nav class="pagination is-centered" role="navigation" aria-label="pagination">

        <a class="pagination-previous"
            :disabled="localPag.prev === undefined"
            @click="changePage(localPag.prev)"
        >Previous</a>
        <a class="pagination-next"
            :disabled="localPag.next === undefined"
            @click="changePage(localPag.next)"
        >Next page</a>

        <ul class="pagination-list">

            <li><a class="pagination-link" aria-label="Goto page 1"
                v-if="localPag.first !== undefined"
                @click="changePage(localPag.first)"
                v-text="localPag.first"
            >1</a></li>

            <li><span class="pagination-ellipsis"
                v-if="localPag.first !== undefined"
            >&hellip;</span></li>

            <li><a class="pagination-link" aria-label="Goto page 45"
                v-if="localPag.prev !== undefined"
                @click="changePage(localPag.prev)"
                v-text="localPag.prev"
            >45</a></li>

            <li><a class="pagination-link is-current" aria-label="Page 46" aria-current="page"
                v-if="localPag.current !== undefined"
                @click="changePage(localPag.current)"
                v-text="localPag.current"
            >46</a></li>

            <li><a class="pagination-link" aria-label="Goto page 47"
                v-if="localPag.next !== undefined"
                @click="changePage(localPag.next)"
                v-text="localPag.next"
            >47</a></li>

            <li><span class="pagination-ellipsis"
                v-if="localPag.last !== undefined"
            >&hellip;</span></li>

            <li><a class="pagination-link" aria-label="Goto page 86"
                v-if="localPag.last !== undefined"
                @click="changePage(localPag.last)"
                v-text="localPag.last"
            >86</a></li>

        </ul>
    </nav>
</template>
<script>
    export default {
        props: ['pagData', 'currentPage'],
        data() {
            return {
                localPagRaw: null,
                localPag: {
                    prev: undefined,
                    next: undefined,
                    current: undefined,
                    first: undefined,
                    last: undefined
                }
            }
        },
        watch: {
            pagData: function() {
                this.localPagRaw = (this.pagData + "").split(",");
                this.localPag = {};
                for(let i = 0; i < this.localPagRaw.length; i++) {
                    let name = this.localPagRaw[i].match(/(?!")(\w+)(?=")/g)[0];
                    let val = this.localPagRaw[i].match(/(?!page=)\d+/g)[0];
                    this.localPag[name] = val;
                }
                this.localPag.current = this.currentPage;
            }
        },
        methods: {
            changePage(page) {
                this.$emit('changePage', page);
            }
        }
    }
</script>
